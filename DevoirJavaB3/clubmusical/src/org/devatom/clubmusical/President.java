package org.devatom.clubmusical;

public class President
{
	private String prenom, nom;
	
	
	
	public President(String nom, String prenom)
	{
		this.prenom = nom;
		this.nom = prenom;
	}
	
	
	
	
	public void setNom(String nom)
	{
		this.nom = nom;
	}
	
	
	
	public void setPrenom(String prenom)
	{
		this.prenom = prenom;
	}
	
	
}
